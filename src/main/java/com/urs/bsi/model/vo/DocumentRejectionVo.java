package com.urs.bsi.model.vo;

public class DocumentRejectionVo {

	private String is_spread_completed;
	private String reject_reason_text;
	private String reject_reason;
	private String status;

	public String getIs_spread_completed() {
		return is_spread_completed;
	}
	public void setIs_spread_completed(String is_spread_completed) {
		this.is_spread_completed = is_spread_completed;
	}
	public String getReject_reason_text() {
		return reject_reason_text;
	}
	public void setReject_reason_text(String reject_reason_text) {
		this.reject_reason_text = reject_reason_text;
	}
	public String getReject_reason() {
		return reject_reason;
	}
	public void setReject_reason(String reject_reason) {
		this.reject_reason = reject_reason;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
